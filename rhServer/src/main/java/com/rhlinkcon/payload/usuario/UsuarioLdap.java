package com.rhlinkcon.payload.usuario;


public class UsuarioLdap {

	private String nome;

	private String login;

	private Long id;

	private String msdsCloudExtensionAttribute11;

	public UsuarioLdap() {

	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getMsdsCloudExtensionAttribute11() {
		return msdsCloudExtensionAttribute11;
	}

	public void setMsdsCloudExtensionAttribute11(String msdsCloudExtensionAttribute11) {
		this.msdsCloudExtensionAttribute11 = msdsCloudExtensionAttribute11;
	}

}
